SYMMETRY_CONVENTIONS = {
    "D": ["D_scipy", "D_rotated"],
    "T": ["T222", "TZ3", "TZ3R"],
    "I": ["I222", "I222R", "IN25", "IN25R", "I2N3", "I2N3R", "I2N5", "I2N5R"],
}

STANDARD_CONVENTIONS = {"D": "D_scipy", "T": "T222", "I": "I222"}

def about_z_rotation(angle):
    """Rotation matrix about the z-axis by given angle (radians)."""
    c = np.cos(angle)
    s = np.sin(angle)
    return np.array([[c, -s, 0], [s, c, 0], [0, 0, 1]], dtype=np.float64)


def sym_family(sym):
    """
    Return the symmetry family key for convention lookups.
    "D3" -> "D", "D5" -> "D", "C3" -> "C", "T" -> "T", "I" -> "I"
    """
    if len(sym) > 1 and sym[0] in ("C", "D"):
        return sym[0]
    return sym

def get_dn_convention_matrices(sym, convention_name):
    """
    Generate rotation matrices for a Dn symmetry convention.

    :param sym: e.g. "D3", "D5"
    :param convention_name: "D_scipy" or "D_rotated"
    :return: (2n, 3, 3) numpy array of rotation matrices
    """
    from scipy.spatial.transform import Rotation as R
    scipy_matrices = R.create_group(sym).as_matrix()
    if convention_name == "D_scipy":
        return scipy_matrices
    if convention_name == "D_rotated":
        n = int(sym[1:])
        M = about_z_rotation(np.pi / (2 * n))
        return np.array([M @ g @ M.T for g in scipy_matrices])
    raise ValueError(f"Unknown Dn convention: {convention_name}")


def get_convention_matrices(sym, convention_name):
    """
    Get rotation matrices for any symmetry convention.
    Works for both static (T, I) and parametric (Dn) conventions.

    :param sym: e.g. "T", "I", "D3", "D5"
    :param convention_name: e.g. "T222", "I222", "D_scipy", "D_rotated"
    :return: (N, 3, 3) numpy array
    """
    if convention_name in SCIPION_SYMMETRIES:
        return SCIPION_SYMMETRIES[convention_name]
    family = sym_family(sym)
    if family == "D":
        return get_dn_convention_matrices(sym, convention_name)
    raise ValueError(f"Unknown convention '{convention_name}' for symmetry '{sym}'")


def conjugation_to_standard(convention_name, sym):
    """
    Return the rotation matrix M that conjugates the given convention to the
    standard one (D_scipy for Dn, T222 for T, I222 for I):
    g_standard = M @ g_source @ M^T.

    :param convention_name: e.g. "TZ3", "IN25R", "D_rotated"
    :param sym: symmetry type, e.g. "T", "I", "D3", "D5"
    :return: (3,3) rotation matrix, or None if already standard
    """
    family = sym_family(sym)
    standard = STANDARD_CONVENTIONS.get(family)
    if standard is None:
        raise ValueError(f"No standard convention defined for symmetry '{sym}'")
    if convention_name == standard:
        return None

    # Fast path for Dn: conjugation matrix is known analytically
    if family == "D" and convention_name == "D_rotated":
        n = int(sym[1:])
        return about_z_rotation(-np.pi / (2 * n))

    source = get_convention_matrices(sym, convention_name)
    target = get_convention_matrices(sym, standard)
    m = find_conjugation_matrix(source, target)
    if m is None:
        raise RuntimeError(
            f"Could not find conjugation matrix from {convention_name} to {standard}")
    return m


def identify_symmetry_convention(volume_path, sym, downsample=64):
    """
    Identify which symmetry convention a downloaded volume corresponds to.

    Tests each candidate convention's rotation matrices against the volume:
    a true symmetry operation leaves the volume unchanged (high FSC).

    :param volume_path: path to the MRC volume file
    :param sym:         symmetry type, e.g. 'T', 'I', 'D3', 'D5'
    :param downsample:  resolution to downsample the volume to (for speed)
    :return:            name of the best-matching convention
    """
    family = sym_family(sym)
    candidates = SYMMETRY_CONVENTIONS.get(family)
    if candidates is None:
        raise ValueError(f"No conventions defined for symmetry '{sym}'. "
                         f"Supported: {list(SYMMETRY_CONVENTIONS.keys())}")

    if len(candidates) == 1:
        logger.info("Symmetry %s has only one convention: %s", sym, candidates[0])
        return candidates[0]

    vol = Volume.load(volume_path).downsample(downsample)

    best_name = None
    best_score = -np.inf
    all_scores = {}

    for name in candidates:
        matrices = get_convention_matrices(sym, name)
        # Skip identity (first element)
        non_identity = matrices[1:]
        scores = []
        for R in non_identity:
            rot = Rotation(R.astype(np.float32).reshape(1, 3, 3))
            rotated_vol = vol.rotate(rot)
            _, fsc_curve = vol.fsc(rotated_vol)
            scores.append(np.mean(fsc_curve))
        avg_score = np.mean(scores)
        all_scores[name] = avg_score
        logger.info("Convention %s: mean FSC = %.6f", name, avg_score)
        if avg_score > best_score:
            best_score = avg_score
            best_name = name
    logger.info("Best convention for %s: %s (mean FSC = %.6f)", sym, best_name, best_score)
    return best_name


def rotation_sets_equal(set_a, set_b, atol=1e-6):
    """
    Check if two sets of rotation matrices are equal (order-independent).
    Returns (match_count, total_a, total_b).
    """
    matched = 0
    for a in set_a:
        for b in set_b:
            if np.allclose(a, b, atol=atol):
                matched += 1
                break
    return matched, len(set_a), len(set_b)


def find_conjugation_matrix(source, target, atol=1e-5):
    """
    Find rotation matrix M such that {M @ g @ M^T : g in source} == target (as sets).

    Picks two source elements with non-parallel axes, tries all compatible target
    pairings, solves for M from the axis mapping, and verifies.

    :param source: (N, 3, 3) array of rotation matrices
    :param target: (N, 3, 3) array of rotation matrices
    :param atol: tolerance for matrix comparison
    :return: M (3x3 rotation matrix) if found, else None
    """
    from scipy.spatial.transform import Rotation as Rot

    n = len(source)
    if len(target) != n:
        return None

    def conjugates_all(m_cand):
        for g in source:
            conj = m_cand @ g @ m_cand.T
            if not any(np.allclose(conj, target[j], atol=atol) for j in range(n)):
                return False
        return True

    # Extract axes and angles
    src_rotvecs = Rot.from_matrix(source).as_rotvec()
    tgt_rotvecs = Rot.from_matrix(target).as_rotvec()
    src_angles = np.linalg.norm(src_rotvecs, axis=1)
    tgt_angles = np.linalg.norm(tgt_rotvecs, axis=1)
    src_axes = np.zeros_like(src_rotvecs)
    tgt_axes = np.zeros_like(tgt_rotvecs)
    for i in range(n):
        if src_angles[i] > atol:
            src_axes[i] = src_rotvecs[i] / src_angles[i]
        if tgt_angles[i] > atol:
            tgt_axes[i] = tgt_rotvecs[i] / tgt_angles[i]

    # Pick two non-identity source elements with non-parallel axes
    s1_idx = None
    s2_idx = None
    for i in range(n):
        if src_angles[i] < atol:
            continue
        if s1_idx is None:
            s1_idx = i
            continue
        if np.linalg.norm(np.cross(src_axes[s1_idx], src_axes[i])) > 0.1:
            s2_idx = i
            break
    if s1_idx is None or s2_idx is None:
        return None

    a1, a2 = src_axes[s1_idx], src_axes[s2_idx]
    theta1, theta2 = src_angles[s1_idx], src_angles[s2_idx]

    tgt_match_1 = [j for j in range(n) if abs(tgt_angles[j] - theta1) < atol and tgt_angles[j] > atol]
    tgt_match_2 = [j for j in range(n) if abs(tgt_angles[j] - theta2) < atol and tgt_angles[j] > atol]

    # Try all compatible (t1, t2) pairings with both axis signs
    for t1_idx in tgt_match_1:
        for sign1 in [1, -1]:
            b1 = sign1 * tgt_axes[t1_idx]
            for t2_idx in tgt_match_2:
                for sign2 in [1, -1]:
                    b2 = sign2 * tgt_axes[t2_idx]
                    # Necessary: angle between axes must be preserved
                    if abs(np.dot(a1, a2) - np.dot(b1, b2)) > 0.01:
                        continue
                    # Build orthonormal bases and solve M
                    u1 = a1
                    u2_raw = a2 - np.dot(a2, u1) * u1
                    u2_n = np.linalg.norm(u2_raw)
                    if u2_n < 1e-8:
                        continue
                    u2 = u2_raw / u2_n
                    u3 = np.cross(u1, u2)

                    v1 = b1
                    v2_raw = b2 - np.dot(b2, v1) * v1
                    v2_n = np.linalg.norm(v2_raw)
                    if v2_n < 1e-8:
                        continue
                    v2 = v2_raw / v2_n
                    v3 = np.cross(v1, v2)

                    m_cand = np.column_stack([v1, v2, v3]) @ np.column_stack([u1, u2, u3]).T
                    if abs(np.linalg.det(m_cand) - 1.0) > 0.01:
                        continue
                    if conjugates_all(m_cand):
                        return m_cand
    return None


